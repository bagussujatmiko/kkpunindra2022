    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
         if(nip.getText().isEmpty() || namaguru.getText().isEmpty() ||tempatlahir.getText().isEmpty()|| tgllahir.getDate()==null || jenkel.getSelectedItem().equals("") ||
            cbnJabatan.getSelectedItem().equals("") || alamat.getText().isEmpty() ||  telephone.getText().isEmpty() ||  username.getText().isEmpty() || password.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Silahkan Lengkapi Data !!");
        }else{
            eg.setNip(nip.getText());
            eg.setNama_guru(namaguru.getText());
            eg.setTemp_lahir(tempatlahir.getText());
            eg.setTgl_lahir (sdf.format(tgllahir.getDate()));
            eg.setJ_kelamin(jenkel.getSelectedItem().toString());
            eg.setJabatan(cbnJabatan.getSelectedItem().toString());
            eg.setAlamat(alamat.getText());
            eg.setTelephone(telephone.getText());
            eg.setUsername(username.getText());
            eg.setPassword(password.getText());
               
            
        if(btntambah.getText().equalsIgnoreCase("batal")){
            if(sm.tambah(eg)==1){
                JOptionPane.showMessageDialog(null, "Simpan Data Berhasil");
            }else{
                JOptionPane.showMessageDialog(null, "Simpan Data Gagal !!");
                }
            tampil();
            }
        
        if(btnedit.getText().equalsIgnoreCase("batal")){
            if(sm.edit(eg)==1){
                JOptionPane.showMessageDialog(null, "Edit Data Berhasil");
            }else{
                JOptionPane.showMessageDialog(null, "Edit Data Gagal !!");
                }
            tampil();
            }
        
        Bersih();
        SiapIsi(false);
        btntambah.setText("Tambah");
        btnedit.setText("Edit");
        tombolAwal();
        cari.setEnabled(true);
        }
         
    }//GEN-LAST:event_btnsimpanActionPerformed

