    private void tblguruMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblguruMouseClicked
        // TODO add your handling code here:
        
        int baris = tblguru.getSelectedRow();
        nip.setText(tblguru.getModel().getValueAt(baris,0).toString());
        namaguru.setText(tblguru.getModel().getValueAt(baris, 1).toString());
        tempatlahir.setText(tblguru.getModel().getValueAt(baris, 2).toString());
        jenkel.setSelectedItem(tblguru.getModel().getValueAt(baris, 3).toString());
        cbnJabatan.setSelectedItem(tblguru.getModel().getValueAt(baris, 4).toString());
        alamat.setText(tblguru.getModel().getValueAt(baris, 5).toString());
        telephone.setText(tblguru.getModel().getValueAt(baris, 6).toString());
        entitas.Entitas_Guru us= sm.cariuser(nip.getText());
        username.setText(us.getUsername());
        password.setText(us.getPassword());
        
        SiapIsi(false);
        btnsimpan.setEnabled(false);
        btntambah.setEnabled(true);
        btntambah.setText("Batal");
        btnhapus.setEnabled(true);
        btnedit.setEnabled(true);
    }//GEN-LAST:event_tblguruMouseClicked

    private void cariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cariKeyReleased
        // TODO add your handling code here:
        String find = null;
        find = cari.getText();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        listGuru.clear();
        listGuru=sm.cari(find);
        for (int i = 0; i < listGuru.size(); i++) {
            Object[] a=new Object[7];
            a[0]=listGuru.get(i).getNip();
            a[1]=listGuru.get(i).getNama_guru();
            a[2]=listGuru.get(i).getAlamat(); 
            a[3]=listGuru.get(i).getJ_kelamin();
            a[4]=listGuru.get(i).getTelephone();
            a[5]=listGuru.get(i).getUsername();
            a[6]=listGuru.get(i).getPassword();
            model.addRow(a);
        }  
    }//GEN-LAST:event_cariKeyReleased

